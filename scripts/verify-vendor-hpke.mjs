import { execFile } from "node:child_process";
import { createHash } from "node:crypto";
import { mkdtemp, readFile, rm } from "node:fs/promises";
import { tmpdir } from "node:os";
import { join } from "node:path";
import { promisify } from "node:util";
import { fileURLToPath } from "node:url";

const run = promisify(execFile);
const ROOT = fileURLToPath(new URL("..", import.meta.url));
const BUNDLE_NAME = "hpke-core-1.9.0.iife.js";
const BUNDLE = join(ROOT, "extension", "vendor", "hpke", BUNDLE_NAME);
const SUMS = join(ROOT, "extension", "vendor", "hpke", "SHA256SUMS");
const GENERATOR = join(ROOT, "scripts", "vendor-hpke.mjs");
const EXPECTED_DEPS = { "@hpke/core": "1.9.0", "@hpke/common": "1.10.1", esbuild: "0.28.1" };

function fail(message) {
  throw new Error(`vendor HPKE verification failed: ${message}`);
}

const pkg = JSON.parse(await readFile(join(ROOT, "package.json"), "utf8"));
for (const [name, version] of Object.entries(EXPECTED_DEPS)) {
  if (pkg.devDependencies?.[name] !== version) fail(`package.json must pin ${name} exactly to ${version}`);
}

const lock = JSON.parse(await readFile(join(ROOT, "package-lock.json"), "utf8"));
const lockedCommon = lock.packages?.["node_modules/@hpke/common"];
if (!lockedCommon) fail("package-lock.json is missing @hpke/common");
if (lockedCommon.version !== EXPECTED_DEPS["@hpke/common"]) fail(`package-lock.json has @hpke/common ${lockedCommon.version || "without a version"}`);
if (!lockedCommon.integrity) fail("package-lock.json @hpke/common is missing registry integrity");

const temp = await mkdtemp(join(tmpdir(), "solstone-hpke-verify-"));
try {
  const generatedA = join(temp, "generated-a.js");
  const generatedB = join(temp, "generated-b.js");
  await run(process.execPath, [GENERATOR, generatedA], { cwd: ROOT });
  await run(process.execPath, [GENERATOR, generatedB], { cwd: ROOT });
  const [a, b, committed] = await Promise.all([readFile(generatedA), readFile(generatedB), readFile(BUNDLE)]);
  if (!a.equals(b)) fail("two locked generator runs produced different bytes");
  if (!a.equals(committed)) fail("generated bytes differ from the committed HPKE bundle");

  const sums = (await readFile(SUMS, "utf8")).trim().split(/\r?\n/).filter(Boolean);
  if (sums.length !== 1) fail("SHA256SUMS must contain exactly one record");
  const match = sums[0].match(/^([0-9a-f]{64})  (.+)$/);
  if (!match || match[2] !== BUNDLE_NAME) fail(`SHA256SUMS must use the standard record for ${BUNDLE_NAME}`);
  const actual = createHash("sha256").update(committed).digest("hex");
  if (actual !== match[1]) fail(`committed checksum ${actual} does not match SHA256SUMS ${match[1]}`);
  console.log(`vendor HPKE verified: ${actual}  ${BUNDLE_NAME}`);
} finally {
  await rm(temp, { recursive: true, force: true });
}
